int test_case;

const ll mod = 1e9+7;

ll dp[1002][1002][2];

ll fun(ll i, ll k, ll n, ll f)
{
	if(i == 0 || k == 1 || i > n) return 0;

	if(dp[i][k][f] != -1) return dp[i][k][f];

	ll op1 = 0, op2 = 0;

	// be aware of crating recursive self loop

	if(f)
	{
		op1 = 1+fun(i-1, k-1, n, !f);
		op2 = fun(i+1, k, n, f);
	}
	else
	{
		op1 = 1+fun(i+1, k-1, n, !f);
		op2 = fun(i-1, k, n, f);		
	}
	return dp[i][k][f] = (op1%mod + op2%mod)%mod;
}


void solve()
{
	memset(dp, -1, sizeof(dp));
    ll n, k; cin >> n >> k;
    ll ans = (fun(1, k, n, 1)+1)%mod;
    cout << ans << "\n";
}

