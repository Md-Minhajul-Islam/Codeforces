int test_case;

void solve()
{
    int n; cin >> n;
    string s; cin >> s;
    int diff = 0, i = 0, k = 1;
    while(1)
    {
    	map<char, int> mp;
    	for(int j = i; j < n; j += k)
    	{
    		mp[s[j]]++;
    		if(mp.size() > 2) break;
    	}
    	if(mp.size() > 2) diff = 10;
    	else if(mp.size() == 2)
    	{
    		int cnt1 = 0, cnt2 = 0;
    		for(auto &x: mp)
    		{
    			if(cnt1 == 0) cnt1 = x.se;
    			else if(cnt2 == 0) cnt2 = x.se;
    		}
    		if(cnt1 == 1) diff++;
    		else if(cnt2 == 1) diff++;
    		else diff = 10;
    	}

    	if(diff > 1 || n%k != 0){diff = 0, k++, i = 0;}
    	else if(i+1 == k) break;
    	else i++;
    }
    cout << k << "\n";
}

