int test_case;

void solve()
{
    int n, c, q; cin >> n >> c >> q;

    string s; cin >> s;
    s = "#"+s;

    vector<pll> v(c+1), ranges(c+1);

    v[0] = {1, n};
    ranges[0] = {1, n};

    for(int i = 1; i <= c; i++)
    {
    	ll l, r; cin >> l >> r;
    	v[i] = {l, r};
    	ranges[i] = {ranges[i-1].se+1ll, ranges[i-1].se+1ll+r-l};
    }
    
    while(q--)
    {
    	ll k; cin >> k;
    	for(int i = c; i >= 0; i--)
    	{
    		if(ranges[i].fi <= k)
    		{
    			k = v[i].fi+k-ranges[i].fi;
    		}
    	}
    	cout << s[k] << "\n";
    }
}

