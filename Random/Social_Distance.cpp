int test_case;

void solve()
{
    int n, k; cin >> n >> k;
    string s; cin >> s;
    vii a; int cnt = 0;
    for(int i = 0; i < n; i++)
    {
    	if(s[i] == '1')
    	{
    		int x = max(0, cnt-k);
    		a.pb(x);
    		cnt = 0;
    	}
    	else cnt++;
    }
    if(cnt != 0) a.pb(cnt);
    int ans = 0;
    if(!a.empty())
    {
    	for(int i = 0, c = 1; i < a[0]; i++, c++)
    	{
    		if(c == 1) ans++;
    		if(c == k+1) c = 0;
    	}
    }
    for(int i = 1; i < a.size(); i++)
    {
    	ans += a[i]/(k+1);
    }
    cout << ans << '\n';     
}

