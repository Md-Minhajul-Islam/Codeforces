int test_case;

void solve()
{
    int a, b; cin >> a >> b;
    set<int> s;
    int p = (a+b+1)/2, q = (a+b)/2;
    for(int x = 0; x <= a+b; x++)
    {
    	int y1 = a-p+x, y2 = q-b+x;
    	if(y1 == y2 && y1 >= 0 && x <= b && y1 <= a) s.insert(x+y1);

    	y1 = b-p+x, y2 = q-a+x;
    	if(y1 == y2 && y1 >= 0 && x <= a && y1 <= b) s.insert(x+y1);
    }
    cout << s.size() << "\n";
    for(auto &x: s) cout << x << " ";
    	cout << "\n";
}

